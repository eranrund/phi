uint16_t phi_crc16_incremental(uint16_t ret, const void * buf, size_t len)
{
	size_t i;

	for (i = 0; i < len; ++i)
	{
		ret = (uint16_t)((uint16_t)((ret << 8) ^ crc_table[(uint16_t)((ret >> 8) ^ ((uint8_t *)buf)[i]) & 0xFFU]) & 0xFFFFU);
	}

	return ret;
}

