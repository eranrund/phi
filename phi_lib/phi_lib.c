uint32_t phi_crc32_incremental(uint32_t crc, const void * buf, size_t len)
{
    const uint8_t *p;
    crc = crc  ^ ~0U;

    p = buf;

    while (len--)
        crc = crc32_table[(crc ^ *p++) & 0xFF] ^ (crc >> 8);

    return crc ^ ~0U;
}
